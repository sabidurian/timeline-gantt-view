var SABIDURIAN_VIEW_TYPE = "sabidurian-timeline";
var SEQUENCE_VIEW_TYPE = "sabidurian-sequence";
var SabidurianPlugin = class extends import_obsidian9.Plugin {
  constructor() {
    super(...arguments);
    this.settings = { ...DEFAULT_SETTINGS };
  }
  async onload() {
    await this.loadSettings();
    this.registerBasesView(SABIDURIAN_VIEW_TYPE, {
      name: "Timeline",
      icon: "lucide-chart-gantt",
      factory: (controller, containerEl) => new SabidurianView(controller, containerEl, this),
      options: getViewOptions
    });
    this.registerBasesView(SEQUENCE_VIEW_TYPE, {
      name: "Sequence",
      icon: "lucide-chart-no-axes-gantt",
      factory: (controller, containerEl) => new SequenceView(controller, containerEl, this),
      options: getSequenceViewOptions
    });
    this.addSettingTab(new SabidurianSettingTab(this.app, this));
    this.app.workspace.trigger("parse-style-settings");
  }
  onunload() {
  }
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
  }
};
